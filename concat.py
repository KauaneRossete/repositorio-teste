# Vamos receber dois dados diferentes do usuário e concatena-los em uma única string.

# Passo 1: Receber os dados do usuário
info1 = input("Digite a primeira informação: ")  # Ex: "Olá"
info2 = input("Digite o segundo informação: ")   # Ex: "Mundo"

# Passo 2: Concatenar os dados
resultado_concat = info1 + " " + info2  # Adicionamos um espaço entre as duas strings

# Passo 3: Exibir o resultado
print("A string concatenada é:", resultado_concat)